# What is a Variable?
# ================================
# A variable in Python is like a container (box) that stores data.
# It has a name (the label on the box).
# It holds a value (the content inside the box).
# You can use it to store, update, or retrieve data later.

# a = 10
# b = 20
#
# print (a + b)
# print (a> b)

